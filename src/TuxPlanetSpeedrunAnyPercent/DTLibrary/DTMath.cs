		public static int NormalizeDegreesScaled(int degreesScaled)
		{
			if (degreesScaled >= 0 && degreesScaled < 360 * 128)
				return degreesScaled;

			if (degreesScaled < 0)
				degreesScaled = degreesScaled + 360 * 128 * 2;

			int multiple = degreesScaled / (360 * 128);

			degreesScaled = degreesScaled - multiple * (360 * 128);

			while (degreesScaled < 0)
				degreesScaled = degreesScaled + 360 * 128;
			while (degreesScaled >= 360 * 128)
				degreesScaled = degreesScaled - 360 * 128;

			return degreesScaled;
		}

		// Given X as input, returns 1024 * sin(X / 128 degrees)
		public static int SineScaled(int degreesScaled)
		{
			if (degreesScaled == int.MinValue)
				return SineScaled(degreesScaled + 360 * 128);

			if (degreesScaled < 0)
				return -SineScaled(-degreesScaled);

			int degreesTimes32 = degreesScaled >> 2;

			int newDegreesTimes32;

			if (degreesTimes32 < 360 * 32)
				newDegreesTimes32 = degreesTimes32;
			else
			{
				int division = degreesTimes32 / (360 * 32);
				newDegreesTimes32 = degreesTimes32 - 360 * 32 * division;
			}

			if (newDegreesTimes32 < 180 * 32)
				return _sineArray[newDegreesTimes32];
			else
			{
				int translatedDegrees = newDegreesTimes32 - 180 * 32;
				return -_sineArray[translatedDegrees];
			}
		}

		// Given X as input, returns 1024 * cos(X / 128 degrees)
		public static int CosineScaled(int degreesScaled)
		{
			if (degreesScaled == int.MinValue)
				return CosineScaled(degreesScaled + 360 * 128);

			if (degreesScaled < 0)
				degreesScaled = -degreesScaled;

			if (degreesScaled >= 360 * 128)
			{
				int division = degreesScaled / (360 * 128);
				degreesScaled = degreesScaled - 360 * 128 * division;
			}

			int newDegrees = degreesScaled + 90 * 128;

			if (newDegrees >= 360 * 128)
				newDegrees -= 360 * 128;

			return SineScaled(newDegrees);
		}

		// Returns arctan(x, y) * 128, using degrees (not radians)
		public static int ArcTangentScaled(int x, int y)
		{
			if (x == 0)
			{
				if (y > 0)
					return 90 * 128;
				else
					return -90 * 128;
			}

			if (y == 0)
			{
				if (x > 0)
					return 0;
				else
					return 180 * 128;
			}

			if (x > 0 && y > 0)
			{
				int result;

				if (x >= 15 * 1000 * 1000 || y >= 15 * 1000 * 1000)
				{
					long xLong = x;
					long yLong = y;

					long resultLong = (yLong * 128L) / xLong;
					if (resultLong >= 1000L * 1000L * 1000L)
						resultLong = 1000L * 1000L * 1000L;
					result = (int)resultLong;
				}
				else
					result = (y << 7) / x;

				if (result >= _arctanArray.Length)
					result = _arctanArray.Length - 1;

				int angle = _arctanArray[result];
				return angle;
			}
			if (x < 0 && y > 0)
			{
				int negativeX;
				if (x == int.MinValue)
					negativeX = int.MaxValue;
				else
					negativeX = -x;
				return 90 * 128 + ArcTangentScaled(y, negativeX);
			}
			if (y < 0)
			{
				int negativeY;
				if (y == int.MinValue)
					negativeY = int.MaxValue;
				else
					negativeY = -y;
				return -ArcTangentScaled(x, negativeY);
			}

			throw new Exception("Unreachable");
		}
